  _loginCall() {
    print("Login method called !!! ");
    String userId = _idCtrl.text;
    String pwd = _pwdCtrl.text;

    User user = new User();
    user.userId = userId;
    user.password = pwd;

    Future future = Server.login(user);
    print("Map is -------  $future");

    future.then((response) {
      var jsonString = response.body;
      var object = json.jsonDecode(jsonString);
      print(object['userid']);
      if (object['userid'] != null) {
        Navigator.pushReplacement(
            context, MaterialPageRoute(builder: (c) => Dashboard(object)));
      }
      else
        setState(() {
                msg = object['msg'];
        });
    }).catchError((err) {
      print(err);
    });
  }

